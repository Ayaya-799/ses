package com.mk.resume.entity;

public class Relatives {
	private String relativesguid;
	private String webuserguid;
	private String companyname;
	private String deptname;
	private String postname;
	private String employeename;

	public String getRelativesguid() {
		return relativesguid;
	}

	public void setRelativesguid(String relativesguid) {
		this.relativesguid = relativesguid;
	}

	public String getWebuserguid() {
		return webuserguid;
	}

	public void setWebuserguid(String webuserguid) {
		this.webuserguid = webuserguid;
	}

	public String getCompanyname() {
		return companyname;
	}

	public void setCompanyname(String companyname) {
		this.companyname = companyname;
	}

	public String getDeptname() {
		return deptname;
	}

	public void setDeptname(String deptname) {
		this.deptname = deptname;
	}

	public String getPostname() {
		return postname;
	}

	public void setPostname(String postname) {
		this.postname = postname;
	}

	public String getEmployeename() {
		return employeename;
	}

	public void setEmployeename(String employeename) {
		this.employeename = employeename;
	}
	
	
	

}
